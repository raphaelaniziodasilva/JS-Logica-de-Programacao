//Existem 3 formas de declarar uma variável
//primeira forma utilizando let
let spaceship = "FE Supernova"
console.log(spaceship)
//podemos atribuir um novo valor quando a variavel ja existe
spaceship = "Super Antiga"
console.log(spaceship)
//vai imprimir os dois valores na tela

//segunda forma utilizando const essa declaração e usada quando agente quer atribuir um valor para uma variavel que nunca vai mudar ou seja o valor e unico
const squad = "Estelar"
console.log(squad)

//terceira forma utilizando var que vai ser a mesma do let
var velocidade = 80
console.log(velocidade)
