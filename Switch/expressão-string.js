//Ultilizando switch com string
let spaceship = "Raptor"

switch(spaceship) {
    case "Golias":
        console.log("Nave lenta")
        break
    case "Raptor":
        console.log("Nave mais resistente")
        break
    case "Elemental":
        console.log("Nave mais rapida")
        break
    default:
        console.log("Nave simples")
        break    
}