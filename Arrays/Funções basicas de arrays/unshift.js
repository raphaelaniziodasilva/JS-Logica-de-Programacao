/* A função .unshift serve para adicionar um elemento na primeira posição do array.
Vamos fazer uma lista e depois adicionar elemento na primeira posição
*/
let lista = ["Controle", "Jogo", 189]
//imprimir lista 
console.log(lista)

//Adicionar um item a nossa lista que é o xbox que esse item vai ser adicionado na primeira posição
lista.unshift("Xbox series x")
//imprimir lista atualizada
console.log(lista)


