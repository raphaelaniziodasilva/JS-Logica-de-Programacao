/* A função .shift ela também retorna o elemento que foi removido:
lista de naves:
*/
let naves = ["Fenix", "Avangers", "Star Wars"]
//imprimir lista atual
console.log(naves)

/* Remover a primeira nave e imprimir o nome da nave que foi removida
*/
let removida = naves.shift()
// imprimir a lista de nave atualizada: a primeira nave foi removida
console.log(naves)
//imprimir o nome da nave que foi removida
console.log(removida)
