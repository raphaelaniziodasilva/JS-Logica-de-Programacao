/* A função .pop serve para remover um elemento que esta na ultima posição do array
Removendo o elemento que esta na ultima posição da lista de naves:
*/
let naves = ["Elemental", "Colossus", "Helmet"]
//imprimir lista de naves atual
console.log(naves)

/* removendo o elemento que esta na ultima posição
remova a ultima nave que esta na lista  
*/
naves.pop()
//imprimir lista de naves atualizada
console.log(naves)

