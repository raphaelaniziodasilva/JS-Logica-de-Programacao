/* A função .shft serve para remover um elemento que esta na primeira posição do array
Removendo o elemento que esta na primeira posição da lista de naves:
*/
let naves = ["Elemental", "Colossus", "Helmet"]
//imprimir lista de naves atual
console.log(naves)

/* removendo o elemento que esta na primeira posição
remova a primeira nave que esta na lista  
*/
naves.shift()
//imprimir lista de naves atualizada
console.log(naves)

